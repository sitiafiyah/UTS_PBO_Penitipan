    private void selectData() {
        String kolom[] = {"id","NamaPetugas","NoResi","NoPol","JenisKendaraan","TanggalMasuk","TanggalKeluar","Harga"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_data";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        
        try{
            while(rs.next()){
                String id = rs.getString(1);
                String NamaPetugas = rs.getString(2);
                String NoResi = rs.getString(3);
                String NoPol = rs.getString(4);
                String JenisKendaraan = "";
                if("Mobil".equals(rs.getString(5))){
                    JenisKendaraan = "Mobil";
                }else {
                    JenisKendaraan = "Motor";
                }
                String TanggalMasuk = rs.getString(6);
                String TanggalKeluar = rs.getString(7);
                String Harga = rs.getString(8);

                String data[] = {id,NamaPetugas,NoResi,NoPol,JenisKendaraan,TanggalMasuk,TanggalKeluar,Harga};
                dtm.addRow(data);
            }
        }catch (SQLException ex){
           // Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblData.setModel(dtm);//To change body of generated methods, choose Tools | Templates.
    }
