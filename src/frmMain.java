    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        int baris =tblData.getSelectedRow();
        if (baris != -1){
           
           txtNama.setText(tblData.getValueAt(baris,1).toString());
           txtNoResi.setText(tblData.getValueAt(baris,2).toString());
           txtNoPol.setText(tblData.getValueAt(baris,3).toString());
           
           String JK = tblData.getValueAt(baris,4).toString();
            if (JK.equals("Laki-laki")){
                rdMobil.setSelected(true);
            }else {
                rdMotor.setSelected(true);
            }
           
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String masuk = tblData.getValueAt(baris,5).toString();
            Date tgl;
            try {
                tgl = dateFormat.parse(masuk);
                TanggalMasuk.setDate(tgl);
            }catch (ParseException ex){
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null,ex);
            }
            
            String keluar = tblData.getValueAt(baris,6).toString();
            Date tgl1;
            try {
                tgl1 = dateFormat.parse(keluar);
                TanggalKeluar.setDate(tgl1);
            }catch (ParseException ex){
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null,ex);
            }
            
            
            txtHarga.setText(tblData.getValueAt(baris,7).toString());
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_tblDataMouseClicked

